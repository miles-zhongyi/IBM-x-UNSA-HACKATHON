export function useUiI18n() {
  const [language, setLanguage] = useState(() => localStorage.getItem(STORAGE_KEY) || "en");

  useEffect(() => {
    const onStorage = () => setLanguage(localStorage.getItem(STORAGE_KEY) || "en");
    const onLangEvent = () => setLanguage(localStorage.getItem(STORAGE_KEY) || "en");
    window.addEventListener("storage", onStorage);
    window.addEventListener("ui-language-change", onLangEvent);
    return () => {
      window.removeEventListener("storage", onStorage);
      window.removeEventListener("ui-language-change", onLangEvent);
    };
  }, []);

  const setUiLanguage = useCallback((nextLanguage) => {
    const next = nextLanguage || "en";
    localStorage.setItem(STORAGE_KEY, next);
    setLanguage(next);
    window.dispatchEvent(new Event("ui-language-change"));
  }, []);

  const t = useCallback(
    (text) => {
      if (!text || language === "en") return text;
      return DICT[language]?.[text] || text;
    },
    [language],
  );

  return { language, setLanguage: setUiLanguage, t };
}

